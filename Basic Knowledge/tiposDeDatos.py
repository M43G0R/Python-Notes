#TIPOS DE DATOS 

#Tipo de dato none
nada = None

#Tipo de dato integer
numeroentero = 1

#Tipo de dato float
numeroflotante = 1.1

#Tipo de dato string
texto = 'Hola amigo'

#Tipo de dato bool
booleano = False

#Tipo de dato array
lista = [1,2,3,4,5]

#Tipo de dato tupla
tupla = (5,4,3,2,1)

#Tipo de dato diccionario
diccionario = {'a':1,'b':2,'c':3,'d':4}

#Tipo de dato range
rango = range(9)

#Tipo de dato byte
datobyte = b"provando"

#Imprime los tipo de datos
print(nada)
print(numeroentero)
print(numeroflotante)
print(texto)
print(booleano)
print(lista)
print(tupla)
print(diccionario)
print(rango)
print(datobyte)